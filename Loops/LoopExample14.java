package Loops;

import java.util.Scanner;

public class LoopExample14 {
	
	public static void main(String[] args) {
		
		
		Scanner sc=new Scanner(System.in);
		for(int i=65;i<=90;i++)
		{
			for(int j=65;j<=90;j++)
			{
				System.out.println((char)i+""+(char)j+"");
			}
			
		}
			
	}

}
